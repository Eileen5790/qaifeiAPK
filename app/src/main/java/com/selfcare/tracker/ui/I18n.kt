val LocalTranslations = compositionLocalOf { zhTranslations }

@Composable
fun LocalizedContent(
    lang: String,
    content: @Composable (Translations) -> Unit
) {
    CompositionLocalProvider(LocalTranslations provides if (lang == "zh") zhTranslations else enTranslations) {
        content(LocalTranslations.current)
    }
}
